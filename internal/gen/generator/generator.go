package generator

import (
	"bytes"
	"context"
	"fmt"
	"io"
	"os"
	"path/filepath"
	"slices"
	"strings"
	"sync"

	"github.com/artem328/depquery-go/internal/gen/schema"

	"github.com/dave/jennifer/jen"
)

type artifact struct {
	f    *jen.File
	name string
}

func Generate(ctx context.Context, workers int, s []*schema.Entity, pkg, dir, source string) error {
	slices.SortStableFunc(s, func(a, b *schema.Entity) int {
		return strings.Compare(a.Name, b.Name)
	})

	ff, err := generate(ctx, workers, s, pkg)
	if err != nil {
		return fmt.Errorf("generate: %w", err)
	}

	if err := os.RemoveAll(dir); err != nil {
		return fmt.Errorf("clear directory: %w", err)
	}

	return writeFiles(ctx, ff, dir, source)
}

func writeFiles(ctx context.Context, ff []artifact, dir, source string) (err error) {
	errs := make(chan error, 1)
	stop := make(chan struct{})
	done := make(chan struct{})

	var wg sync.WaitGroup

	for _, f := range ff {
		wg.Add(1)

		go func() {
			defer wg.Done()
			fileWriter(ctx, f, errs, stop, dir, source)
		}()
	}

	go func() {
		defer close(done)
		wg.Wait()
	}()

	select {
	case <-ctx.Done():
		close(stop)

		err = ctx.Err()
	case err = <-errs:
		close(stop)

		err = fmt.Errorf("emit: %w", err)

		select {
		case <-ctx.Done():
		case <-done:
		}
	case <-done:
		err = nil
	}

	return err
}

func fileWriter(ctx context.Context, f artifact, errs chan<- error, stop <-chan struct{}, dir, source string) {
	select {
	case <-ctx.Done():
		return
	case <-stop:
		return
	default:
	}

	addHeader(f.f, source)

	if err := outputFile(ctx, dir, f.name, f.f); err != nil {
		errs <- err
	}
}

func addHeader(f *jen.File, source string) {
	f.HeaderComment("Code generated by github.com/artem328/depquery-go/cmd/depquerygen. DO NOT EDIT.")
	f.HeaderComment("Schema source at " + source)
}

func outputFile(ctx context.Context, dir, name string, jf *jen.File) error {
	if err := os.MkdirAll(dir, 0755); err != nil {
		return err
	}

	buf := bytes.NewBuffer(nil)
	if err := jf.Render(buf); err != nil {
		return err
	}

	f, err := os.OpenFile(filepath.Join(dir, name+".go"), os.O_WRONLY|os.O_CREATE|os.O_TRUNC, 0644)
	if err != nil {
		return err
	}

	done := make(chan struct{})
	defer close(done)

	go func() {
		select {
		case <-ctx.Done():
			f.Close()
		case <-done:
		}
	}()

	defer f.Close()

	if _, err := io.Copy(f, buf); err != nil {
		return err
	}

	return nil
}
