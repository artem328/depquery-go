package jen

import (
	"bytes"
	"io"

	"github.com/artem328/depquery-go/internal/gen/plan"
	. "github.com/dave/jennifer/jen"
)

type Renderer struct {
	plan           plan.Plan
	pkg            string
	schemaLocation string
	naming         naming
	types          types
	members        members
	f              *File
}

func NewRenderer(p plan.Plan, outDir, pkg, schemaLocation string) *Renderer {
	var n naming

	n.warmUp(p)

	return &Renderer{
		plan:           p,
		pkg:            pkgName(outDir, pkg),
		schemaLocation: schemaLocation,
		naming:         n,
		types:          warmUpTypes(p),
		members:        warmUpMembers(p),
	}
}

func (r *Renderer) RenderBuilders() (io.Reader, error) {
	r.f = NewFile(r.pkg)

	r.renderBuilders()

	buf := bytes.NewBuffer(make([]byte, 0, 4096))
	if err := r.f.Render(buf); err != nil {
		return nil, err
	}

	return buf, nil
}

func (r *Renderer) RenderDepquery() (io.Reader, error) {
	r.f = NewFile(r.pkg)

	r.renderDepquery()

	buf := bytes.NewBuffer(make([]byte, 0, 4096))
	if err := r.f.Render(buf); err != nil {
		return nil, err
	}

	return buf, nil
}

func (r *Renderer) renderBuilders() {
	r.renderHeader()

	r.renderCompilerInterface()

	r.renderBuilderInterfaces()

	r.renderCandidateInterface()

	r.renderCompilerDefaultConfig()

	r.renderBuildContext()

	r.renderBuilderImplementations()
}

func (r *Renderer) renderDepquery() {
	r.renderHeader()

	r.renderPrefetchResolverInterface()

	r.renderEntityPrefetcherInterface()

	r.renderStateInterface()

	r.renderInstanceInterface()

	r.renderPlanInterface()

	r.renderStateImplementation()

	r.renderInstanceImplementation()

	r.renderPlanImplementation()

	r.renderFetchContextImplementation()

	r.renderResolvers()
}

func (r *Renderer) renderHeader() {
	r.f.HeaderComment("Code generated by github.com/artem328/depquery-go/cmd/depquerygen. DO NOT EDIT.")
	r.f.HeaderComment("Schema source at " + r.schemaLocation)
}
